#ifdef HAVE_CONFIG_H
#include "config.h"
#endif

#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <syslog.h>

#ifndef HAVE_SETENV
int
setenv(name, value, overwrite)
	const char *name;
	const char *value;
	int overwrite;
{
	size_t  len;
	int     ret;
	char   *p;

	if (overwrite == 0 && getenv(name))
		return 0;

	if (value == NULL)
	    value = "";

	len = strlen(name) + strlen(value) + 2;
	p = (char *) malloc(len);
	if (p == NULL)
		return -1;

	(void) sprintf(p, "%s=%s", name, value);
	ret = putenv(p);
	return ret;
}
#endif
